    public static class IdMapGenerator
    {
        #region 파싱된 데이터 구조

        private class ParsedItemData
        {
            public List<(string itemCode, int itemId)> CurrencyItems = new();
            public List<(string itemCode, int itemId)> ConsumableItems = new();
            public List<(string itemCode, int itemId)> MaterialItems = new();
            public List<int> CharacterIds = new();
            public List<int> CharacterPieceIds = new();
        }

        private class ParsedElpisBuildData
        {
            public List<(int buildId, FacilityType facilityType, int slotIndex)> Builds = new();
        }

        #endregion

        [MenuItem("Tools/Generate IdMap")]
        public static void Generate()
        {
            // OriginalSpecData.json 경로
            string jsonPath = Path.Combine(Application.dataPath, "OriginalSpecData.json");
            if (!File.Exists(jsonPath))
            {
                Debug.LogError($"OriginalSpecData.json not found at: {jsonPath}");
                return;
            }

            // JSON 파일 읽기
            string jsonText = File.ReadAllText(jsonPath);
            var data = Newtonsoft.Json.JsonConvert.DeserializeObject<Dictionary<string, object>>(jsonText);

            // 데이터 한 번에 파싱
            var itemData = ParseItemData(data);
            var elpisData = ParseElpisBuildData(data);

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated>");
            sb.AppendLine("// IdMap - 자동 생성된 ID 맵입니다.");
            sb.AppendLine("// Assets > Tools > Generate IdMap 메뉴로 재생성할 수 있습니다.");
            sb.AppendLine("// </auto-generated>");
            sb.AppendLine();
            sb.AppendLine("namespace CookApps.AutoBattler");
            sb.AppendLine("{");
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    /// 각종 ID에 쉽게 접근하기 위한 정적 클래스");
            sb.AppendLine("    /// 사용 예: IdMap.Item.Gold, IdMap.ElpisBuild.CommandCenter");
            sb.AppendLine("    /// </summary>");
            sb.AppendLine("    public static partial class IdMap");
            sb.AppendLine("    {");

            // Item 중첩 클래스 생성
            GenerateItemClass(sb, itemData);

            sb.AppendLine();

            // ElpisBuild 중첩 클래스 생성
            GenerateElpisBuildClass(sb, elpisData);

            sb.AppendLine("    }");
            sb.AppendLine();

            // ItemId Extension Methods 생성
            GenerateItemIdExtensions(sb, itemData);

            sb.AppendLine();

            // CheatCurrencyType enum 생성
            GenerateCheatCurrencyTypeEnum(sb, itemData);

            sb.AppendLine("}");

            // 파일 저장
            string outputPath = Path.Combine(Application.dataPath, "_Project/Scripts/Spec/IdMap.cs");
            File.WriteAllText(outputPath, sb.ToString());

            AssetDatabase.Refresh();
            Debug.Log($"IdMap.cs generated at: {outputPath}");
        }

        #region 데이터 파싱

        private static ParsedItemData ParseItemData(Dictionary<string, object> data)
        {
            var result = new ParsedItemData();

            // ItemCurrencyTable 처리
            if (data.ContainsKey("ItemCurrencyTable"))
            {
                var table = Newtonsoft.Json.JsonConvert.DeserializeObject<List<ItemCurrencyData>>(data["ItemCurrencyTable"].ToString());
                foreach (var item in table)
                {
                    if (!string.IsNullOrEmpty(item.item_code))
                    {
                        result.CurrencyItems.Add((item.item_code, item.currency_id));
                    }
                }
            }

            // ItemConsumableTable 처리
            if (data.ContainsKey("ItemConsumableTable"))
            {
                var table = Newtonsoft.Json.JsonConvert.DeserializeObject<List<ItemConsumableData>>(data["ItemConsumableTable"].ToString());
                foreach (var item in table)
                {
                    if (!string.IsNullOrEmpty(item.item_code))
                    {
                        result.ConsumableItems.Add((item.item_code, item.item_id));
                    }
                }
            }

            // ItemMaterialTable 처리
            if (data.ContainsKey("ItemMaterialTable"))
            {
                var table = Newtonsoft.Json.JsonConvert.DeserializeObject<List<ItemMaterialData>>(data["ItemMaterialTable"].ToString());
                foreach (var item in table)
                {
                    if (!string.IsNullOrEmpty(item.item_code))
                    {
                        result.MaterialItems.Add((item.item_code, item.item_id));
                    }

                    if (item.material_type == (int)MaterialType.CHARACTER)
                    {
                        result.CharacterIds.Add(item.item_id);
                    }
                    else if (item.material_type == (int)MaterialType.CHARACTER_PIECE)
                    {
                        result.CharacterPieceIds.Add(item.item_id);
                    }
                }
            }

            return result;
        }

        private static ParsedElpisBuildData ParseElpisBuildData(Dictionary<string, object> data)
        {
            var result = new ParsedElpisBuildData();

            if (data.ContainsKey("ElpisBuildInfo"))
            {
                var table = Newtonsoft.Json.JsonConvert.DeserializeObject<List<ElpisBuildData>>(data["ElpisBuildInfo"].ToString());

                // build_id 기준으로 고유한 값만 추출
                var uniqueBuilds = new Dictionary<int, (FacilityType facilityType, int slotIndex)>();
                foreach (var item in table)
                {
                    if (!uniqueBuilds.ContainsKey(item.build_id))
                    {
                        uniqueBuilds[item.build_id] = ((FacilityType)item.facility_type, item.slot_index);
                    }
                }

                foreach (var kvp in uniqueBuilds)
                {
                    result.Builds.Add((kvp.Key, kvp.Value.facilityType, kvp.Value.slotIndex));
                }
            }

            return result;
        }

