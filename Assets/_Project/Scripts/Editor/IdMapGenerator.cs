        #region 코드 생성

        private static void GenerateItemClass(StringBuilder sb, ParsedItemData itemData)
        {
            sb.AppendLine("        public static partial class Item");
            sb.AppendLine("        {");

            // Currency 섹션
            sb.AppendLine("            // ==================== Currency Items ====================");
            foreach (var (itemCode, itemId) in itemData.CurrencyItems)
            {
                sb.AppendLine($"            public static readonly ItemId {itemCode} = {itemId};");
            }

            sb.AppendLine();
            sb.AppendLine("            // ==================== Consumable Items ====================");
            foreach (var (itemCode, itemId) in itemData.ConsumableItems)
            {
                sb.AppendLine($"            public static readonly ItemId {itemCode} = {itemId};");
            }

            sb.AppendLine();
            sb.AppendLine("            // ==================== Material Items ====================");
            foreach (var (itemCode, itemId) in itemData.MaterialItems)
            {
                sb.AppendLine($"            public static readonly ItemId {itemCode} = {itemId};");
            }

            sb.AppendLine("        }");
        }

        private static void GenerateItemIdExtensions(StringBuilder sb, ParsedItemData itemData)
        {
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    /// ItemId 확장 메서드");
            sb.AppendLine("    /// </summary>");
            sb.AppendLine("    public static class ItemIdExtensions");
            sb.AppendLine("    {");

            // IsCurrency 함수 생성
            var currencyIds = itemData.CurrencyItems.Select(x => x.itemId).ToList();
            GenerateExtensionMethod(sb, "IsCurrency", currencyIds);

            // IsConsumable 함수 생성
            var consumableIds = itemData.ConsumableItems.Select(x => x.itemId).ToList();
            GenerateExtensionMethod(sb, "IsConsumable", consumableIds);

            // IsMaterial 함수 생성
            var materialIds = itemData.MaterialItems.Select(x => x.itemId).ToList();
            GenerateExtensionMethod(sb, "IsMaterial", materialIds);

            // IsCharacter 함수 생성
            GenerateExtensionMethod(sb, "IsCharacter", itemData.CharacterIds);

            // IsCharacterPiece 함수 생성
            GenerateExtensionMethod(sb, "IsCharacterPiece", itemData.CharacterPieceIds);

            // GetCharacterId 함수 생성
            GenerateGetCharacterIdMethod(sb);

            // GetCharacterPieceId 함수 생성
            GenerateGetCharacterPieceIdMethod(sb, itemData.CharacterIds, itemData.CharacterPieceIds);

            sb.AppendLine("    }");
        }

        private static void GenerateGetCharacterIdMethod(StringBuilder sb)
        {
            sb.AppendLine("        public static bool GetCharacterId(this ItemId id, out int charId)");
            sb.AppendLine("        {");
            sb.AppendLine("            charId = -1;");
            sb.AppendLine("            if (id.IsCharacter())");
            sb.AppendLine("            {");
            sb.AppendLine("                charId = id.Value;");
            sb.AppendLine("                return true;");
            sb.AppendLine("            }");
            sb.AppendLine("            if (id.IsCharacterPiece())");
            sb.AppendLine("            {");
            sb.AppendLine("                charId = id.Value % 10000;");
            sb.AppendLine("                return true;");
            sb.AppendLine("            }");
            sb.AppendLine("            return false;");
            sb.AppendLine("        }");
            sb.AppendLine();
        }

        private static void GenerateGetCharacterPieceIdMethod(StringBuilder sb, List<int> characterIds, List<int> characterPieceIds)
        {
            // characterId -> characterPieceId 매핑 생성
            var mapping = new Dictionary<int, int>();
            foreach (int charId in characterIds)
            {
                int pieceId = characterPieceIds.FirstOrDefault(p => p % 10000 == charId);
                if (pieceId != 0)
                {
                    mapping[charId] = pieceId;
                }
            }

            sb.AppendLine("        public static ItemId GetCharacterPieceId(int characterId)");
            sb.AppendLine("        {");
            sb.AppendLine("            return characterId switch");
            sb.AppendLine("            {");

            foreach (var kvp in mapping.OrderBy(x => x.Key))
            {
                sb.AppendLine($"                {kvp.Key} => {kvp.Value},");
            }

            sb.AppendLine("                _ => default");
            sb.AppendLine("            };");
            sb.AppendLine("        }");
            sb.AppendLine();
        }

        private static void GenerateExtensionMethod(StringBuilder sb, string methodName, List<int> ids)
        {
            sb.AppendLine($"        public static bool {methodName}(this ItemId id)");
            sb.AppendLine("        {");

            if (ids.Count == 0)
            {
                sb.AppendLine("            return false;");
            }
            else
            {
                sb.Append("            return ");
                for (int i = 0; i < ids.Count; i++)
                {
                    if (i > 0)
                        sb.Append(" || ");
                    sb.Append($"id == {ids[i]}");
                }
                sb.AppendLine(";");
            }

            sb.AppendLine("        }");
            sb.AppendLine();
        }

        private static void GenerateElpisBuildClass(StringBuilder sb, ParsedElpisBuildData elpisData)
        {
            // FacilityType별로 그룹화
            var groupedBuilds = elpisData.Builds
                .GroupBy(b => b.facilityType)
                .OrderBy(g => (int)g.Key)
                .ToList();

            sb.AppendLine("        public static class ElpisBuild");
            sb.AppendLine("        {");

            foreach (var group in groupedBuilds)
            {
                sb.AppendLine($"            // ==================== {group.Key} ====================");

                var sortedBuilds = group.OrderBy(b => b.slotIndex).ThenBy(b => b.buildId).ToList();

                if (sortedBuilds.Count == 1)
                {
                    var build = sortedBuilds[0];
                    string name = GetFacilityName(build.facilityType);
                    sb.AppendLine($"            public const int {name} = {build.buildId};");
                }
                else
                {
                    for (int i = 0; i < sortedBuilds.Count; i++)
                    {
                        var build = sortedBuilds[i];
                        string name = GetFacilityName(build.facilityType);
                        sb.AppendLine($"            public const int {name}_{i + 1} = {build.buildId};");
                    }
                }

                sb.AppendLine();
            }

            // 마지막 빈 줄 제거
            sb.Length -= System.Environment.NewLine.Length;

            sb.AppendLine("        }");
        }

        private static string GetFacilityName(FacilityType facilityType)
        {
            return facilityType switch
            {
                FacilityType.COMMAND_CENTER => "CommandCenter",
                FacilityType.NEST => "Nest",
                FacilityType.DIMENSION_LAB => "DimensionLab",
                FacilityType.SIMULATION_CENTER => "SimulationCenter",
                _ => $"Unknown_{(int)facilityType}"
            };
        }

        private static void GenerateCheatCurrencyTypeEnum(StringBuilder sb, ParsedItemData itemData)
        {
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    /// 치트용 재화 타입 (SROptions 드롭다운용)");
            sb.AppendLine("    /// </summary>");
            sb.AppendLine("    public enum CheatCurrencyType");
            sb.AppendLine("    {");

            foreach (var (itemCode, itemId) in itemData.CurrencyItems)
            {
                sb.AppendLine($"        {itemCode} = {itemId},");
            }

            foreach (var (itemCode, itemId) in itemData.ConsumableItems)
            {
                sb.AppendLine($"        {itemCode} = {itemId},");
            }

            // 캐릭터 추가
            if (itemData.CharacterIds.Count > 0)
            {
                sb.AppendLine();
                sb.AppendLine("        // 캐릭터");
                foreach (int charId in itemData.CharacterIds.OrderBy(x => x))
                {
                    sb.AppendLine($"        캐릭터_{charId} = {charId},");
                }
            }

            // 캐릭터 조각 추가
            if (itemData.CharacterPieceIds.Count > 0)
            {
                sb.AppendLine();
                sb.AppendLine("        // 캐릭터 조각");
                foreach (int pieceId in itemData.CharacterPieceIds.OrderBy(x => x % 10000))
                {
                    int characterId = pieceId % 10000;
                    sb.AppendLine($"        조각_{characterId} = {pieceId},");
                }
            }

            sb.AppendLine("    }");
        }

